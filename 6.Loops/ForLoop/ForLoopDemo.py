# method2 : using range() function
for i in range(10):
    print(i)

for i in range(0, 10):
    print(i)

for i in range(0, 10, 2):
    print(i)

for i in range(10, 0, -1):
    print(i)

for i in range(10, 0, -2):
    print(i)